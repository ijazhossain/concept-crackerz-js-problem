/* 
You will have a function which takes the length and width of a rectangle as parameters. Find out the area of that rectangle and return the result.
[
Note:
Input format: two integer numbers as input
Output format: a single integer number
]
Sample Input:
10 5
Sample output:
50
*/
function findArea(length, width) {
    return length * width;
}
const result = findArea(10, 5);
console.log(result);